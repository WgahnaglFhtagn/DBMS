    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 
        try {
        if (jTable1.getSelectedRow() >= 0) {
            NewBook nb = new NewBook((Frame) this.getParent(), true, connection, 
                    model.getSelectesItem(jTable1.getSelectedRow()));
            nb.setVisible(true);
            model.updateData();
            ((AbstractTableModel) jTable1.getModel()).fireTableDataChanged();
        }
        } catch (SQLException e)
        {}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            if (jTable1.getSelectedRow() >= 0) {
                Statement statement = connection.createStatement();
                statement.executeUpdate("delete from book where id = "
                        + model.getSelectesItem(jTable1.getSelectedRow()).getId()
                        + ";");
            }
            model.updateData();
            ((AbstractTableModel) jTable1.getModel()).fireTableDataChanged();
        } catch (SQLException ex) {
            Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

